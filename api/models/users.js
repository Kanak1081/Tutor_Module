const mongoose = require('mongoose')

//User Schema
const userSchema = mongoose.Schema({
    id:Number,
    name : {
        type:String,
        required:true
    },
    email:{
        type:String,
        required:true,
        unique:true,
        match:/[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*@(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?/
    },
    password:{
        type:String,
        required:true
    }
})


module.exports = mongoose.model("User",userSchema);


